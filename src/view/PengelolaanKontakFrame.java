    private void loadContacts() {
    try {
        model.setRowCount(0); // Bersihkan tabel
        List<Kontak> contacts = controller.getAllContacts();

        for (Kontak c : contacts) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNama(),
                c.getNomorTelepon(),
                c.getKategori()
            });
        }
    } catch (SQLException e) {
        showError(e.getMessage());
    }
}

